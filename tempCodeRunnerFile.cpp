/*
 * এই প্রোগ্রামটি OpenGL এবং GLUT ব্যবহার করে স্ক্রিনে কয়েকটি বড় সাদা বিন্দু এবং মাঝখানে এক্স ও ওয়াই অক্ষ (axes) আকারে পয়েন্টসমূহ আঁকে।
 *
 * 1. display() ফাংশনে:
 *    - প্রথমে ব্যাকগ্রাউন্ড পরিষ্কার করা হয়।
 *    - চারটি বড় সাদা পয়েন্ট  (Point size=23) চারটি কোণে (-0.5, -0.5), (-0.5, 0.5), (0.5, -0.5), (0.5, 0.5) ড্র করা হয়।
 *    - এরপর পয়েন্ট সাইজ ছোট করে (3.0) এক্স অক্ষ বরাবর ছোট লিলাক (magenta) পয়েন্ট ও ওয়াই অক্ষ বরাবর একই রঙের পয়েন্ট ড্র করা হয়।
 *    - এক্স অক্ষের জন্য x কে -1 থেকে 1 পর্যন্ত 0.001 ইঞ্চি বাড়িয়ে প্রতিটি পজিশনে পয়েন্ট দেয়া হয়,
 *      ওয়াই অক্ষের জন্য y কে -1 থেকে 1 পর্যন্ত 0.001 ইঞ্চি বাড়িয়ে প্রতিটি পজিশনে পয়েন্ট দেয়া হয়।
 * 
 * 2. main() ফাংশনে:
 *    - উইন্ডো সাইজ 840x840 পিক্সেল ও নাম "Draw Points" সেট করা হয়।
 *    - display() ফাংশনকে রেন্ডারিং কলব্যাক হিসেবে সেট করা হয়।
 *    - GLUT এর মেইন লুপ চালু করা হয়।
 *
 * এই প্রোগ্রামটি OpenGL-এ পয়েন্ট ড্র করার একটি সহজ উদাহরণ, যেখানে বড় পয়েন্ট এবং পয়েন্ট লাইন (axes) প্রদর্শিত হয়।
 */


 #include <GL/glut.h>   // OpenGL Utility Toolkit লাইব্রেরি

// ডিসপ্লে ফাংশন যা উইন্ডোতে ড্রয়িং করবে
void display()
{
    glClear(GL_COLOR_BUFFER_BIT);  // পূর্বের ছবি মুছে নতুন ছবি আঁকা শুরু

    // বড় বড় পয়েন্টের জন্য রঙ সেট করলাম (সাদা)
    glColor3f(1.0, 1.0, 1.0);

    // পয়েন্টের সাইজ বড় করলাম (23 পিক্সেল)
    glPointSize(23.0);

    glBegin(GL_POINTS);  // পয়েন্ট ড্রয়িং শুরু

    // চারটি পয়েন্ট নির্দিষ্ট করলাম - চারটি ভের্টেক্সে
    glVertex2f(0.5, -0.5);
    glVertex2f(-0.5, 0.5);
    glVertex2f(0.5, 0.5);
    glVertex2f(-0.5, -0.5);

    glEnd();  // পয়েন্ট ড্রয়িং শেষ

    // এবার ছোট পয়েন্টের জন্য সাইজ সেট করলাম
    glPointSize(3.0);

    glBegin(GL_POINTS);  // আবার পয়েন্ট ড্রয়িং শুরু

    // x-অক্ষ বরাবর ছোট পয়েন্ট আঁকা (দৈর্ঘ্য -1 থেকে 1)
    for (float x = -1.0; x <= 1.0; x += 0.001)
    {
        glColor3f(1.0, 0.0, 1.0);  // ম্যাজেন্টা (গাঢ় গোলাপি) রঙ
        glVertex2f(x, 0.0);        // y=0 রেখায় পয়েন্ট দেওয়া হচ্ছে
    }

    // y-অক্ষ বরাবর ছোট পয়েন্ট আঁকা (দৈর্ঘ্য -1 থেকে 1)
    for (float y = -1.0; y <= 1.0; y += 0.001)
    {
        glVertex2f(0.0, y);        // x=0 রেখায় পয়েন্ট দেওয়া হচ্ছে
    }

    glEnd();  // পয়েন্ট ড্রয়িং শেষ

    glFlush();  // OpenGL কমান্ড শেষ করে রেন্ডারিং দেখায়
}

int main(int argc, char** argv)
{
    glutInit(&argc, argv);  // GLUT ইনিশিয়ালাইজ
    glutInitDisplayMode(GLUT_SINGLE | GLUT_RGB);  // সিঙ্গেল বাফার ও RGB মোড
    glutInitWindowSize(840, 840);  // উইন্ডোর সাইজ সেট
    glutCreateWindow("Draw Points");  // উইন্ডো তৈরি ও শিরোনাম
    glutDisplayFunc(display);  // ডিসপ্লে ফাংশন সেট
    glutMainLoop();  // প্রোগ্রাম চালু ও ইভেন্ট লুপ শুরু

    return 0;  // প্রোগ্রাম সফল সমাপ্তি
}
