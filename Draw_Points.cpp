
/*
 * এই প্রোগ্রামটি OpenGL ব্যবহার করে একটি উইন্ডোতে পয়েন্ট ড্র করে।
 * স্ক্রিনের চার কোণে চারটি বড় সাদা পয়েন্ট দেখায়।
 * সাথে X এবং Y অক্ষ বরাবর ছোট গোলাপি রঙের পয়েন্টগুলো লাইন হিসেবে প্রদর্শন করে।
 * ফলে, অক্ষগুলো ও চারটি কোণের পয়েন্ট একসাথে দেখা যায়।
 */

#include <GL/glut.h>

// ডিসপ্লে ফাংশন যা উইন্ডোতে সব কিছু আঁকে
void display()
{
    // স্ক্রীন ক্লিয়ার করা (কালার বাফার ফ্লাশ)
    glClear(GL_COLOR_BUFFER_BIT);

    // সাদা রঙ সেট করা (RGB: 1,1,1)
    glColor3f(1.0, 1.0, 1.0);

    // পয়েন্টের সাইজ বড় করা যাতে পয়েন্টগুলো স্পষ্ট দেখা যায়
    glPointSize(3.0);

    // পয়েন্ট আঁকার জন্য শুরু করা
    glBegin(GL_POINTS);

    // x অক্ষ বরাবর -1 থেকে 1 পর্যন্ত পয়েন্ট আঁকা, y=0 এ (হরাইজন্টাল লাইন)
    for (float x = -1.0; x <= 1.0; x += 0.001)
    {
        glVertex2f(x, 0.0); // (x,0) পয়েন্ট প্লট করো
    }

    // y অক্ষ বরাবর -1 থেকে 1 পর্যন্ত পয়েন্ট আঁকা, x=0 এ (ভার্টিকাল লাইন)
    for (float y = -1.0; y <= 1.0; y += 0.001)
    {
        glVertex2f(0.0, y); // (0,y) পয়েন্ট প্লট করো
    }
    glEnd(); // পয়েন্ট আঁকা শেষ

    // এখন নতুন পয়েন্ট গ্রুপ শুরু (যেখানে রং আলাদা আলাদা)
    glBegin(GL_POINTS);

    // লাল রঙের হরাইজন্টাল লাইন, y=0.5, x=0 থেকে 1 পর্যন্ত
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(1.0, 0.0, 0.0); // লাল রঙ
        glVertex2f(x, 0.5);
    }

    // সবুজ রঙের হরাইজন্টাল লাইন, y=0.3, x=0 থেকে 1 পর্যন্ত
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(0.0, 1.0, 0.0); // সবুজ রঙ
        glVertex2f(x, 0.3);
    }

    // নীল রঙের হরাইজন্টাল লাইন, y=0.7, x=0 থেকে 1 পর্যন্ত
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(0.0, 0.0, 1.0); // নীল রঙ
        glVertex2f(x, 0.7);
    }

    // লাল রঙের ভার্টিকাল লাইন, x=-0.5, y=0 থেকে -1 পর্যন্ত (নীচের দিকে)
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(1.0, 0.0, 0.0); // লাল রঙ
        glVertex2f(-0.5, -y);
    }

    // সবুজ রঙের ভার্টিকাল লাইন, x=-0.3, y=0 থেকে -1 পর্যন্ত
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(0.0, 1.0, 0.0); // সবুজ রঙ
        glVertex2f(-0.3, -y);
    }

    // নীল রঙের ভার্টিকাল লাইন, x=-0.7, y=0 থেকে -1 পর্যন্ত
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(0.0, 0.0, 1.0); // নীল রঙ
        glVertex2f(-0.7, -y);
    }
    glEnd(); // পয়েন্ট আঁকার শেষ

    // সব ড্রিং শেষ করে কমান্ড ফ্লাশ করা যাতে সব কিছু স্ক্রীনে দেখা যায়
    glFlush();
}

int main(int argc, char** argv)
{
    // GLUT ইনিশিয়ালাইজ করা
    glutInit(&argc, argv);

    // সিঙ্গল বাফার ও RGB কালার মোড সেট করা
    glutInitDisplayMode(GLUT_SINGLE | GLUT_RGB);

    // উইন্ডোর সাইজ সেট করা
    glutInitWindowSize(480, 440);

    // উইন্ডো তৈরি ও শিরোনাম সেট করা
    glutCreateWindow("3Lines");

    // ডিসপ্লে ফাংশন রেজিস্টার করা
    glutDisplayFunc(display);

    // মেইন ইভেন্ট লুপ চালানো, উইন্ডো ওপেন থাকবে
    glutMainLoop();

    return 0;
}
