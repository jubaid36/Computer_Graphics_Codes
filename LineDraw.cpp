/*
 * এই প্রোগ্রামটি OpenGL এর মাধ্যমে একটি উইন্ডোতে দুইটি লাইন ড্র করে।
 * প্রথম লাইনগুলো হল লাল রঙের X এবং Y অক্ষ (horizontal এবং vertical axes)।
 * দ্বিতীয় লাইনটি একটি সাদা রঙের রেখা যা কেন্দ্র থেকে (0,0) থেকে (0.5, 0.5) পয়েন্টে যায়।
 * ফলে, স্ক্রিনে অক্ষদন্ড এবং একটি কোণের দিকে সাদা রেখা দেখা যাবে।
 */
#include <GL/glut.h>  // OpenGL Utility Toolkit লাইব্রেরি ইমপোর্ট

// ডিসপ্লে ফাংশন যা স্ক্রীনে ড্রয়িং করে
void display()
{
    // স্ক্রীনটি পরিষ্কার করা, যাতে আগের ড্রয়িংগুলো মুছে যায়
    glClear(GL_COLOR_BUFFER_BIT);

    // লাল রঙের জন্য শুরু করা লাইন ড্রয়িং
    glBegin(GL_LINES);

    // লাল রঙ সেট করা (RGB: 1.0, 0.0, 0.0)
    glColor3f(1.0, 0.0, 0.0);

    // X অক্ষ বরাবর লাইন আঁকা: বাম থেকে ডান (-1.0,0.0) থেকে (1.0,0.0)
    glVertex2f(-1.0, 0.0);
    glVertex2f(1.0, 0.0);

    // Y অক্ষ বরাবর লাইন আঁকা: নিচ থেকে উপরে (0.0,-1.0) থেকে (0.0,1.0)
    glVertex2f(0.0, -1.0);
    glVertex2f(0.0, 1.0);

    glEnd(); // লাইন ড্রয়িং শেষ

    // এবার সাদা রঙের ছোট লাইন আঁকা শুরু
    glBegin(GL_LINES);

    // সাদা রঙ সেট করা (RGB: 1.0, 1.0, 1.0)
    glColor3f(1.0, 1.0, 1.0);

    // (0,0) থেকে (0.5,0.5) পর্যন্ত একটি সরল রেখা আঁকা (ডায়াগোনাল লাইন)
    glVertex2f(0.0, 0.0);
    glVertex2f(0.5, 0.5);

    glEnd(); // সাদা লাইন ড্রয়িং শেষ

    // সমস্ত ড্রয়িং ফ্লাশ করা যাতে স্ক্রীনে দেখা যায়
    glFlush();
}

int main(int argc, char** argv)
{
    glutInit(&argc, argv);  // GLUT ইনিশিয়ালাইজেশন

    // একক বাফার এবং RGB কালার মোড সেট করা
    glutInitDisplayMode(GLUT_SINGLE | GLUT_RGB);

    // উইন্ডোর আকার নির্ধারণ করা (500x500 পিক্সেল)
    glutInitWindowSize(500, 500);

    // উইন্ডোর শিরোনাম সহ উইন্ডো তৈরি
    glutCreateWindow("Line Drawing");

    // ডিসপ্লে ফাংশন রেজিস্টার করা, যা স্ক্রীনে ড্রয়িং করবে
    glutDisplayFunc(display);

    // GLUT এর মেইন লুপ চালু, যা ইভেন্টগুলো হ্যান্ডেল করবে
    glutMainLoop();

    return 0;
}
