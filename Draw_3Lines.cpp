/*
=====================================================================================
    প্রোগ্রামটি OpenGL ব্যবহার করে বিভিন্ন রঙের সরলরেখা (Lines) এবং অক্ষরেখা (Axes) 
    আঁকবে পয়েন্টগুলোর মাধ্যমে।

    কাজের বর্ণনা:

    1. প্রথমে X ও Y অক্ষগুলো আঁকা হয়:
       - X অক্ষ: -1 থেকে 1 পর্যন্ত, y=0 রেখায় সাদা রঙের পয়েন্ট।
       - Y অক্ষ: -1 থেকে 1 পর্যন্ত, x=0 রেখায় সাদা রঙের পয়েন্ট।

    2. এরপর ৩টি হরিজন্টাল (Horizontal) লাইন ড্র করা হয়, যেগুলো আলাদা আলাদা রঙের:
       - লাল রঙে y=0.5 রেখায়, x=0 থেকে 1 পর্যন্ত।
       - সবুজ রঙে y=0.3 রেখায়, x=0 থেকে 1 পর্যন্ত।
       - নীল রঙে y=0.7 রেখায়, x=0 থেকে 1 পর্যন্ত।

    3. তারপর ৩টি ভের্টিক্যাল (Vertical) লাইন ড্র করা হয়, একইভাবে বিভিন্ন রঙের:
       - লাল রঙে x=-0.5 রেখায়, y=0 থেকে -1 পর্যন্ত (নীচের দিকে)।
       - সবুজ রঙে x=-0.3 রেখায়, y=0 থেকে -1 পর্যন্ত।
       - নীল রঙে x=-0.7 রেখায়, y=0 থেকে -1 পর্যন্ত।

    4. পয়েন্টগুলোর সাইজ 3.0 নির্ধারিত হয়েছে যাতে পয়েন্টগুলো স্পষ্ট দেখা যায়।

    5. প্রতিটি লাইনকে পয়েন্টের ধারাবাহিক অবস্থান হিসেবে ধরা হয়েছে, 
       যেখানে প্রতি 0.001 করে পয়েন্ট প্লট করা হয়েছে।

    মূল উদ্দেশ্য:
    - OpenGL এ পয়েন্ট ব্যবহার করে বিভিন্ন লাইন ড্র করার প্রাথমিক ধারণা দেওয়া।
    - RGB রঙ ব্যবহারের মাধ্যমে ভিজ্যুয়াল পার্থক্য দেখানো।

=====================================================================================
*/

#include <GL/glut.h>

void display()
{
    glClear(GL_COLOR_BUFFER_BIT);   // স্ক্রিন ক্লিয়ার

    glPointSize(3.0);               // পয়েন্ট সাইজ সেট

    // সাদা রঙে x এবং y অক্ষ ড্র করব
    glColor3f(1.0, 1.0, 1.0);
    glBegin(GL_POINTS);
    for (float x = -1.0; x <= 1.0; x += 0.001)
        glVertex2f(x, 0.0);         // x-axis

    for (float y = -1.0; y <= 1.0; y += 0.001)
        glVertex2f(0.0, y);         // y-axis
    glEnd();

    // এখন রঙিন লাইনগুলো ড্র করব
    glBegin(GL_POINTS);

    // ডান পাশে ৩টি হরিজন্টাল লাইন
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(1.0, 0.0, 0.0);  // লাল
        glVertex2f(x, 0.5);
    }
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(0.0, 1.0, 0.0);  // সবুজ
        glVertex2f(x, 0.3);
    }
    for (float x = 0.0; x <= 1.0; x += 0.001)
    {
        glColor3f(0.0, 0.0, 1.0);  // নীল
        glVertex2f(x, 0.7);
    }

    // বাম নিচে ৩টি ভের্টিকাল লাইন
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(1.0, 0.0, 0.0);  // লাল
        glVertex2f(-0.5, -y);
    }
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(0.0, 1.0, 0.0);  // সবুজ
        glVertex2f(-0.3, -y);
    }
    for (float y = 0.0; y <= 1.0; y += 0.001)
    {
        glColor3f(0.0, 0.0, 1.0);  // নীল
        glVertex2f(-0.7, -y);
    }
    glEnd();

    glFlush();  // ড্র শেষ করে রেন্ডার করো
}

int main(int argc, char** argv)
{
    glutInit(&argc, argv);
    glutInitDisplayMode(GLUT_SINGLE | GLUT_RGB);
    glutInitWindowSize(480, 440);
    glutCreateWindow("3Lines");
    glutDisplayFunc(display);
    glutMainLoop();
    return 0;
}
